// comparison operator 
// < , > , <= , >=   
// == , != // value
// === , !== // value and type 

console.log(2 === '2')
console.log(2 !== '2')
console.log(2 === 2)
console.log(2 !== 2)
console.log(2 == '2')
console.log(2 != '2')
console.log(2 != 2)
console.log(7 > 3)
console.log(7 < 3)
console.log(7 >= 3)
console.log(7 <= 3)
console.log(7 >= 7)
console.log(7 <= 7)


// logical operator 
//&&
//  true   &&   true  -------- true 
console.log(7 == 7 && 8 === 8)
console.log(6 === '6' && 7 === 7)
console.log(6 === 6  && 7 !== 7)
console.log(5 == 4 && 8 == 9)

// || 
// false  || false -----> false

console.log(7 == 7 || 8 === 8)
console.log(6 === '6' || 7 === 7)
console.log(6 === 6  || 7 !== 7)
console.log(5 == 4 || 8 == 9)
